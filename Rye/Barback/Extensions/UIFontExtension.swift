import Foundation
import UIKit

extension UIFont {

    class func primaryFont() -> String {
        return "Karla-Regular"
    }

    class func heavyFont() -> String {
        return "Karla-Bold"
    }

    class func emphasisFont() -> String {
        return "Karla-BoldItalic"
    }
}
